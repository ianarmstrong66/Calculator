package com.qa.calculator;

public class Calculator {

	public int add(int number1, int number2) {
		int result = number1 + number2;
		return result;
	}

	public int subtract(int number1, int number2) {

		int result = number1 - number2;
		return result;
	}

	public int multipley(int number1, int number2) {

		int result = number1 * number2;
		return result;
	}
}